    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        
        boolean placaEncontrada = false;
        String nombrepSerch = txtNombreP.getText();
    
        for(int i =0; i<Fortaleza.programas.size(); i++){
            if(Fortaleza.programas.get(i).getNombreP().equals(nombrepSerch)){
                txtNombreP.setText(Fortaleza.programas.get(i).getNombreP());
                txtDepartAca.setText(Fortaleza.programas.get(i).getDepartamento());
               txtCodigo.setText(Fortaleza.programas.get(i).getCodigoP());
               txtMateria1.setText(Fortaleza.programas.get(i).getMateria1());
               txtMateria2.setText(Fortaleza.programas.get(i).getMateria2());
               txtMateria3.setText(Fortaleza.programas.get(i).getMateria3());
                placaEncontrada =true;
            }
        }
            
        if(placaEncontrada == false){
        Alerta("No se encontrÃ³ Programa en el registro!", "Error"); 
        
        txtNombreP.setText("");
        txtDepartAca.setText("");
        txtCodigo.setText("");
        txtMateria1.setText("");
        txtMateria2.setText("");
        txtMateria3.setText("");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

